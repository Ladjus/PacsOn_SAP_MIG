-- v4, 2026-01-29: Added kus.makulerad AS KUS_MAK and le.makulerad AS LE_MAK.
-- v5, 2026-02-02: Updat lista med FR.ftgnr från "Harmonisering leverantörer aktiva 2024-2025.xlsx".
SELECT
    CONCAT(FR.FtgNr, '#', FR.ForetagKod) as KUNNR_LIFNR,    -- Unique key

    CASE 
        WHEN kus.FtgNr IS NOT NULL THEN 'CUST'  -- Om det är en "riktig" kund KUS-tabell så 'CUST' (Customer)
        WHEN lp.OrdLevPlats1 IS NOT NULL THEN 'SHPT'  -- Om det är en leveransplatskund LP-tabell så 'SHPT'(Customer: Ship-to party, only)
        ELSE ''
    END AS KTOKD,    -- Customer Account Group
    CASE 
        WHEN kus.makulerad IS NOT NULL THEN kus.makulerad
        ELSE ''
    END AS KUS_MAK,  -- Kund makuleringsflagga

    CASE 
        WHEN le.FtgNr IS NOT NULL THEN 'SUPL'  -- Om leverantör LE-tabell så 'SUPL' (Supplier)
        ELSE ''
    END AS KTOKK,    -- Supplier Account Group
    CASE 
        WHEN le.makulerad IS NOT NULL THEN le.makulerad
        ELSE ''
    END AS LE_MAK,  -- Leverantör makuleringsflagga

    FR.ftgnamn AS NAMORG1,        -- Företagsnamn

    CASE
        WHEN LEN(FR.ftgpostadr1) > 1 THEN FR.ftgpostadr1  -- Ej skräptecken.
        ELSE ''
    END AS NAMORG2,    -- C/O-adress field contains Name2 data

    CASE
        WHEN LEN(FR.comnr) > 1 THEN FR.comnr  -- Ej skräptecken.
        ELSE ''
    END AS SORTL,    -- Sökbegrepp

    -- FR.q_fr_sokord as MCOD2,    -- Söknamn

    CASE
        WHEN LEN(FR.orgnr) > 1 THEN FR.orgnr  -- Ej skräptecken.
            CASE FR.landskod
                WHEN 'SE' THEN REPLACE(FR.orgnr, '-', '')  -- Sweden remove '-' for SAP SE2 tax # format.
                ELSE FR.orgnr
            END
        ELSE ''
    END AS FR_orgnr,    -- Organisationsnummer

    CASE
        WHEN LEN(FR.eumomsnr) > 1 THEN FR.eumomsnr  -- Ej skräptecken.
        ELSE ''
    END AS FR_eumomsnr,    -- VAT reg no

    -- Gatuadress
    CASE
        WHEN UPPER(LEFT(TRIM(FR.ftgpostadr2),3)) <> 'BOX' THEN FR.ftgpostadr2
        WHEN UPPER(LEFT(TRIM(FR.ftgpostadr2),3)) = 'BOX' AND FR.ftgpostadr5 IS NOT NULL THEN FR.ftgpostadr5
        WHEN UPPER(LEFT(TRIM(FR.ftgpostadr2),3)) = 'BOX' AND FR.ftgpostadr4 IS NOT NULL THEN FR.ftgpostadr4
        WHEN FR.ftgpostadr2 IS NULL AND FR.ftgpostadr5 IS NOT NULL THEN FR.ftgpostadr5
        ELSE ''
    END AS STREET,    -- Gatuadress gata

    CASE 
        WHEN UPPER(LEFT(TRIM(FR.ftgpostadr2),3)) <> 'BOX' THEN
            CASE
                WHEN FR.landskod = 'SE' AND LEN(FR.ftgpostnr) = 6 THEN FR.ftgpostnr  -- Sverige "NNN NN"
                WHEN FR.landskod = 'SE' AND LEN(FR.ftgpostnr) = 5 THEN CONCAT(LEFT(FR.ftgpostnr,3),' ',RIGHT(FR.ftgpostnr,2))  -- Sverige "NNNNN" --> "NNN NN"
                ELSE FR.ftgpostnr
            END
        WHEN UPPER(LEFT(TRIM(FR.ftgpostadr2),3)) = 'BOX' AND FR.ftgpostadr5 IS NOT NULL THEN FR.ftglevpostnr
        WHEN UPPER(LEFT(TRIM(FR.ftgpostadr2),3)) = 'BOX' AND FR.ftgpostadr4 IS NOT NULL THEN FR.ftgbespostnr
        WHEN FR.ftgpostadr2 IS NULL AND FR.ftgpostadr5 IS NOT NULL THEN FR.ftglevpostnr
        ELSE ''
    END AS POST_CODE1,    -- Gatuadress postnummer

    CASE 
        WHEN UPPER(LEFT(TRIM(FR.ftgpostadr2),3)) <> 'BOX' THEN FR.ftgpostadr3
        WHEN UPPER(LEFT(TRIM(FR.ftgpostadr2),3)) = 'BOX' AND FR.ftgpostadr5 IS NOT NULL THEN FR.ftgpostlevadr3
        WHEN UPPER(LEFT(TRIM(FR.ftgpostadr2),3)) = 'BOX' AND FR.ftgpostadr4 IS NOT NULL THEN FR.ftgpostbesadr3
        WHEN FR.ftgpostadr2 IS NULL AND FR.ftgpostadr5 IS NOT NULL THEN FR.ftgpostlevadr3
        ELSE ''
    END AS CITY1,    -- Gatuadress stad

    -- Boxadress
    CASE 
        WHEN UPPER(LEFT(TRIM(FR.ftgpostadr2),3)) = 'BOX' THEN FR.ftgpostadr2
        ELSE ''
    END AS PO_BOX,    -- Boxadress box

    CASE 
        WHEN UPPER(LEFT(TRIM(FR.ftgpostadr2),3)) = 'BOX' THEN FR.ftgpostnr
        ELSE ''
    END AS POST_CODE2,    -- Boxadress postnummer

    CASE 
        WHEN UPPER(LEFT(TRIM(FR.ftgpostadr2),3)) = 'BOX' THEN FR.ftgpostadr3
        ELSE ''
    END AS PO_BOX_LOC,    -- Boxadress stad

    FR.landskod AS COUNTRY,    -- Land
    
    CASE
        WHEN LEN(FR.ean_loc_code) = 13 THEN LEFT(FR.ean_loc_code,7)  -- GS1/GLN-nummer: Om FR.ean_loc_code innehåller 13 siffror så tag tecken 1-7
        ELSE ''
    END AS LOCATION_1,    -- International Location Number  (Part 1)

    CASE 
        WHEN LEN(FR.ean_loc_code) = 13 THEN SUBSTRING(FR.ean_loc_code, 8, 5)  -- GS1/GLN-nummer: Om FR.ean_loc_code innehåller 13 siffror så tag tecken 8-12
        ELSE ''
    END AS LOCATION_2,    -- International Location Number  (Part 1)

    CASE 
        WHEN LEN(FR.ean_loc_code) = 13 THEN RIGHT(FR.ean_loc_code, 1)  -- GS1/GLN-nummer: Om FR.ean_loc_code innehåller 13 siffror så tag tecken 13
        ELSE ''
    END AS LOCATION_3,    -- International Location Number  (Part 1)

    CONCAT(FR.ftgnr, '#', FR.foretagkod) AS BPEXT,    -- External business-partner number
    FR.landskod AS LANGU_CORR,    -- Språk SE --> SV etc

    CASE 
        WHEN LEN(FR.webadress) > 1 THEN 'HPG'  -- Webb-URL och "HPG" om inte ett skräptecken
        ELSE ''
    END AS URI_TYP,

    CASE 
        WHEN LEN(FR.webadress) > 1 THEN FR.webadress
        ELSE ''
    END AS URI_ADDR

  CASE
        WHEN LEN(KUS.q_buyer_code) > 1 THEN KUS.q_buyer_code  -- Ej skräptecken.
        ELSE ''
    END AS YY1_CUSTOMERNUMBERFF_CUS    -- Factor France Customer Number

FROM FR
LEFT OUTER JOIN KUS ON   -- Ref KUS.q_buyer_code
    KUS.ForetagKod = FR.ForetagKod
    AND KUS.FtgNr = FR.FtgNr
LEFT OUTER JOIN lp ON
    lp.ForetagKod = FR.ForetagKod
    AND lp.OrdLevPlats1 = FR.FtgNr
LEFT OUTER JOIN le ON
    le.ForetagKod = FR.ForetagKod
    AND le.FtgNr = FR.FtgNr
WHERE FR.ForetagKod IN (2000, 6000, 9000, 9100, 9400, 9500)    -- Mall, Ö, AB, V, N, S
